public class FightSong {

    public static void goTeamGo() {
        System.out.println("Go, team, go!\n" +
                           "You can do it.");
    }

    public static void bestWest() {
        System.out.println("You're the best,\n" +
                           "In the West.");
    }

    public static void main(String[] args) {
        goTeamGo();
        System.out.println();

        goTeamGo();
        bestWest();
        goTeamGo();
        System.out.println();

        goTeamGo();
        bestWest();
        goTeamGo();
        System.out.println();

        goTeamGo();
    }
}
